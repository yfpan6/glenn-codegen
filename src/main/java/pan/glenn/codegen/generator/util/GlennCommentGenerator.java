    public static void addFieldComment(Field field,
                                       DomainField domainField,
                                       String[] commoneLines) {
        if (GlobalConfig.getString("suppress.all.comments") != null) {
            return;
        }

        StringBuilder sb = new StringBuilder();

        field.addJavaDocLine("/**"); //$NON-NLS-1$

        sb.append(" * "); //$NON-NLS-1$
        if (!StringUtil.isBLank(domainField.getFieldComment())) {
            sb.append(domainField.getFieldComment());
        } else if (!StringUtil.isBLank(domainField.getFieldCNName())) {
            sb.append(domainField.getFieldCNName());
        } else {
            sb.append(domainField.getColumnName());
        }
        field.addJavaDocLine(sb.toString());
        sb.setLength(0);

        addCommentLine(field, sb, commoneLines);

        sb.append(" * "); //$NON-NLS-1$
        sb.append(domainField.getDomainConfig().getFullyQualifiedTableText());
        sb.append('.');
        sb.append(domainField.getColumnName());
        field.addJavaDocLine(sb.toString());

        addJavadocTag(field, false);

        field.addJavaDocLine(" */"); //$NON-NLS-1$
    }

    public static void addFieldComment(Field field,
                                DomainField domainField) {
        addFieldComment(field, domainField, null);
    }

