    /**
     * This method returns a formated date string to include in the Javadoc tag
     * and XML comments. You may return null if you do not want the date in
     * these documentation elements.
     *
     * @return a string representing the current timestamp, or null
     */
    protected static String getDateString() {
        if (GlobalConfig.getString("suppress.date") != null) {
            return null;
        } else {
            return new Date().toString();
        }
    }

    /**
     *
     * @param innerClass
     * @param domainConfig String catalog, String schema, String tableName, char separator
     *                    eg: separator=.  catalog.schema.tableName
     */
    public static void addClassComment(InnerClass innerClass,
                                       DomainConfig domainConfig,
                                       String[] commoneLines) {
        if (GlobalConfig.getString("suppress.all.comments") != null) {
            return;
        }

        StringBuilder sb = new StringBuilder();

        innerClass.addJavaDocLine("/**"); //$NON-NLS-1$
        sb.append(" * " + domainConfig.getEntityClassCNName());
        innerClass.addJavaDocLine(sb.toString());
        sb.setLength(0);


        addCommentLine(innerClass, sb, commoneLines);

        sb.append(" * This class corresponds to the database table "); //$NON-NLS-1$
        sb.append(domainConfig.getFullyQualifiedTableText());
        innerClass.addJavaDocLine(sb.toString());
        sb.setLength(0);

        innerClass.addJavaDocLine(" *");

        sb.append(" * @Author ").append(GlobalConfig.getString("file.comment.author"));
        innerClass.addJavaDocLine(sb.toString());
        sb.setLength(0);

        sb.append(" * @Date ").append(GlobalConfig.getString("date.today"));
        innerClass.addJavaDocLine(sb.toString());

        addJavadocTag(innerClass, false);

        innerClass.addJavaDocLine(" */"); //$NON-NLS-1$
    }

    private static void addCommentLine(JavaElement je, StringBuilder sb, String[] commoneLines) {
        if (commoneLines != null && commoneLines.length > 0) {
            for (String line : commoneLines) {
                sb.append(" * ").append(line);
                je.addJavaDocLine(sb.toString());
                sb.setLength(0);
            }
        }
    }
    public static void addJavaComment(JavaElement javaElement,
                                      DomainConfig domainConfig) {
        addJavaComment(javaElement, domainConfig, null);
    }

